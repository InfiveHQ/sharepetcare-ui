-- Check all current RLS policies
-- Run this first to see what policies exist

-- Check pets table policies
SELECT 
    schemaname,
    tablename,
    policyname,
    permissive,
    roles,
    cmd,
    qual,
    with_check
FROM pg_policies 
WHERE tablename = 'pets'
ORDER BY policyname;

